const screen = { xs: 2, md: 3, lg: 4, xl: 6 };
let currentPage = 0;

function renderPage(pageIndex) {
  const spotItem = spot[pageIndex];
  const paginationDiv = document.getElementById("pagination");
  paginationDiv.innerHTML = `
      <h3 class="text-center">${spotItem.title}</h3>
      <div class="row justify-content-center">
          ${spotItem.images
            .map(
              (img, index) => `<div class="col-6 col-md-4 col-lg-3 col-xl-${
                12 / screen.xl
              }">
          <img src="${
            spotItem.path + img
          }" class="img-fluid mb-3 dynamicImage w-100" alt="${
                spotItem.title
              }" onclick="openModal('${spotItem.path + img}', ${index})"></div>`
            )
            .join("")}
      </div>
      <div class="description rounded">
          <p>${spotItem.description}</p>
      </div>
      <div class="row align-items-center">
          <div class="col-md-6">
              <div class="embed-responsive embed-responsive-16by9 mb-3">
                  <iframe class="embed-responsive-item" id="mapFrame" src="${
                    spotItem.address.site
                  }" width="100%" height="100%" allowfullscreen></iframe>
              </div>
          </div>
          <div class="col-md-6 text-center">
              <div>
                  <p><strong>Address:</strong> ${spotItem.address.location}</p>
                  <p><strong>Entrance Fee:</strong> ${spotItem.fee}</p>
                  <p><strong>Contact Number:</strong> <a href="tel:${
                    spotItem.contact.number
                  }">${spotItem.contact.number}</a></p>
                  <div class="d-flex justify-content-center">
                      <button class="btn btn-secondary btn-small me-2" onclick="locateMap('${
                        spotItem.title
                      }')">${spotItem.address.button}</button>
                      <button class="btn btn-secondary btn-small" onclick="contact('${
                        spotItem.contact.number
                      }')">${spotItem.contact.button}</button>
                  </div>
              </div>
          </div>
      </div>
  `;

  // Make images clickable for opening the modal
  document.querySelectorAll(".dynamicImage").forEach((image, index) => {
    image.addEventListener("click", () => {
      openModal(spotItem.path + spotItem.images[index]);
    });
  });
}

function updatePaginationControls() {
  const controls = document.getElementById("pagination-controls");
  const maxPages = spot.length;

  controls.innerHTML = `
      <li class="page-item my-1">
          <a class="page-link" href="#anchorPage" aria-label="Previous" onclick="changePage(currentPage - 1)">
              <span aria-hidden="true">&laquo;</span>
          </a>
      </li>
  `;
  for (let i = 0; i < Math.min(maxPages, spot.length); i++) {
    controls.innerHTML += `<li class="page-item ${
      i === currentPage ? "active" : ""
    } mx-1 my-1"><a class="page-link" href="#anchorPage" onclick="changePage(${i})">${
      i + 1
    }</a></li>`;
  }
  controls.innerHTML += `
      <li class="page-item my-1">
          <a class="page-link" href="#anchorPage" aria-label="Next" onclick="changePage(currentPage + 1)">
              <span aria-hidden="true">&raquo;</span>
          </a>
      </li>
  `;
}

function changePage(pageIndex) {
  if (pageIndex < 0 || pageIndex >= spot.length) return;
  currentPage = pageIndex;
  renderPage(currentPage);
  updatePaginationControls();
}

function locateMap(title) {
  const query = encodeURIComponent(title);
  const url = `https://www.google.com/maps/search/?api=1&query=${query}`;
  window.open(url, "_blank");
}

function contact(number) {
  Swal.fire({
    title: "Contact Information",
    html: `
          <p>Contact number: ${number}</p>
          <button class="btn btn-primary" onclick="callNow('${number}')">CALL NOW</button>
          <button class="btn btn-secondary" onclick="copyNumber('${number}')">COPY NUMBER</button>
      `,
    icon: "info",
    showCloseButton: true,
    showConfirmButton: false,
  });
}

function openModal(imageSrc) {
  const modalImage = document.getElementById("modalImage");
  modalImage.style.maxWidth = "90vw";
  modalImage.src = imageSrc;
  const imageModal = new bootstrap.Modal(document.getElementById("imageModal"));
  imageModal.show();

  imageModal._element.addEventListener("hidden.bs.modal", () => {
    document.body.style.opacity = 1; // Fix for opacity issue
  });
}

function callNow(number) {
  window.location.href = `tel:${number}`;
}

function copyNumber(number) {
  navigator.clipboard.writeText(number).then(() => {
    Swal.fire("Copied!", "Contact number copied to clipboard.", "success");
  });
}

document.addEventListener("DOMContentLoaded", () => {
  renderPage(currentPage);
  updatePaginationControls();
  window.addEventListener("resize", updatePaginationControls); // Update pagination on window resize
});
